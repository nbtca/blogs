<h3 id="技巧2-精确搜索" tabindex="-1"><a class="header-anchor" href="#技巧2-精确搜索" aria-hidden="true">#</a> 技巧2：精确搜索</h3>
<p><strong>方式</strong>：给关键词加双引号，<code v-pre>&quot;关键词&quot;</code></p>
<p><strong>功能</strong>：只搜索引号里的字，少一个字或者把字拆开都不行</p>
<p><strong>示例</strong>：</p>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>"达拉不崩吧"
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><blockquote>
<p>注意是英文的双引号</p>
</blockquote>
<h3 id="技巧3-指定网站内搜索" tabindex="-1"><a class="header-anchor" href="#技巧3-指定网站内搜索" aria-hidden="true">#</a> 技巧3：指定网站内搜索</h3>
<p><strong>方式</strong>：<code v-pre>site:域名 关键词</code></p>
<p><strong>功能</strong>：只搜索指定域名的内容</p>
<p><strong>示例</strong>：</p>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>site:zhihu.com 前端
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p>有一些站内自带的搜索并不好用，可以尝试这个搜索技巧。</p>
<h3 id="技巧4-指定文件格式" tabindex="-1"><a class="header-anchor" href="#技巧4-指定文件格式" aria-hidden="true">#</a> 技巧4：指定文件格式</h3>
<p><strong>方式</strong>：<code v-pre>filetype:文件格式 关键词</code></p>
<p><strong>示例</strong>：</p>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>filetype:avi 你懂的
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p>常见文件格式有<code v-pre>doc</code>、<code v-pre>pdf</code>、<code v-pre>ppt</code>等</p>
<h3 id="技巧5-指定在标题搜索" tabindex="-1"><a class="header-anchor" href="#技巧5-指定在标题搜索" aria-hidden="true">#</a> 技巧5：指定在标题搜索</h3>
<p><strong>方式</strong>：<code v-pre>intitle:关键词</code></p>
<p><strong>功能</strong>：搜索的关键词要出现在标题当中</p>
<h3 id="技巧6-指定在内容中搜索" tabindex="-1"><a class="header-anchor" href="#技巧6-指定在内容中搜索" aria-hidden="true">#</a> 技巧6：指定在内容中搜索</h3>
<p><strong>方式</strong>：<code v-pre>intext:关键词</code>、<code v-pre>allintext:关键词1 关键词2</code>、<code v-pre>inurl:关键词2</code></p>
<h3 id="技巧7-多种搜索技巧叠加使用" tabindex="-1"><a class="header-anchor" href="#技巧7-多种搜索技巧叠加使用" aria-hidden="true">#</a> 技巧7：多种搜索技巧叠加使用</h3>
