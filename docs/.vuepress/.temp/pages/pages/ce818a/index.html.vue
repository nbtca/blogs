<h3 id="技巧1-排除干扰项" tabindex="-1"><a class="header-anchor" href="#技巧1-排除干扰项" aria-hidden="true">#</a> 技巧1：排除干扰项</h3>
<p><strong>方式</strong>：关键词1+空格+减号+关键词2，即 <code v-pre>关键词1 -关键词2</code></p>
<p><strong>功能</strong>：排除一些不想要的关键词</p>
